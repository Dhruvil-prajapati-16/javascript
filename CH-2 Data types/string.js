let surname = "Prajapati"

console.log(`Hello ${name} ${surname}`); // backtip left side of number 1

// ============================================
//  SPECIAL CHARACTERS
// ============================================
/*
CHARACTER    MEANING              EXAMPLE
---------    -------              -------
\n           New line             "Hello\nWorld"
\t           Tab                  "Name:\tJohn"
\'           Single quote         'I\'m happy'
\"           Double quote         "He said \"Hi\""
\\           Backslash            "C:\\folder\\file"
\r           Carriage return      "Line1\r\nLine2"
*/
console.log("Hello\nWorld");      // Hello (newline) World
console.log("Name:\tJohn");        // Name:    John
console.log('I\'m happy');         // I'm happy
console.log("He said \"Hi\"");     // He said "Hi"
console.log("C:\\folder\\file");   // C:\folder\file
